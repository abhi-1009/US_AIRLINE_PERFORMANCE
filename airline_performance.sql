-- Create View 
DROP VIEW IF EXISTS vw_flights_enriched;
CREATE VIEW vw_flights_enriched AS
SELECT
-- Date & Time
    f.FLIGHT_DATE, f.YEAR, f.MONTH, f.DAY, f.DAY_OF_WEEK, f.DEPARTURE_HOUR, f.SCHEDULED_DEP_TIME,
    f.ACTUAL_DEP_TIME, f.SCHEDULED_ARR_TIME, f.ACTUAL_ARR_TIME,
-- Flight Identity
    f.AIRLINE AS AIRLINE_CODE, al.AIRLINE AS AIRLINE_NAME, f.FLIGHT_NUMBER, f.TAIL_NUMBER,
-- Origin Airport
    f.ORIGIN_AIRPORT AS ORIGIN_CODE, orig.AIRPORT AS ORIGIN_AIRPORT_NAME, orig.CITY AS ORIGIN_CITY,
    orig.STATE AS ORIGIN_STATE, orig.LATITUDE AS ORIGIN_LAT, orig.LONGITUDE AS ORIGIN_LON,
-- Destination Airport
    f.DESTINATION_AIRPORT AS DEST_CODE, dest.AIRPORT AS DEST_AIRPORT_NAME, dest.CITY AS DEST_CITY,
    dest.STATE AS DEST_STATE, dest.LATITUDE AS DEST_LAT, dest.LONGITUDE AS DEST_LON, f.DISTANCE,
-- Delay Metrics
    f.DEPARTURE_DELAY, f.ARRIVAL_DELAY, f.IS_DELAYED, f.AIR_SYSTEM_DELAY, f.SECURITY_DELAY, f.AIRLINE_DELAY,
    f.LATE_AIRCRAFT_DELAY, f.WEATHER_DELAY,
-- Status
    f.CANCELLED, f.CANCELLATION_REASON, f.CANCELLATION_REASON_DESC, f.DIVERTED,
-- Operational
    f.TAXI_OUT, f.TAXI_IN, f.AIR_TIME, f.ELAPSED_TIME, f.SCHEDULED_TIME
FROM flights f
LEFT JOIN airlines al   ON f.AIRLINE             = al.IATA_CODE
LEFT JOIN airports orig ON f.ORIGIN_AIRPORT      = orig.IATA_CODE
LEFT JOIN airports dest ON f.DESTINATION_AIRPORT = dest.IATA_CODE;

