ALTER TABLE flights ADD COLUMN IS_DELAYED TINYINT(1) NOT NULL DEFAULT 0, ADD COLUMN CANCELLATION_REASON_DESC VARCHAR(40);
DELIMITER $$
CREATE PROCEDURE update_is_delayed()
BEGIN
    DECLARE i INT DEFAULT 1;
    WHILE i <= 6000000 DO
        UPDATE flights
        SET IS_DELAYED = CASE 
            WHEN ARRIVAL_DELAY > 15 AND CANCELLED = 0 THEN 1 
            ELSE 0 END
        WHERE flight_id BETWEEN i AND i + 499999;
        SET i = i + 500000;
    END WHILE;
END$$
DELIMITER $$
CREATE PROCEDURE update_cancel_desc()
BEGIN
    DECLARE i INT DEFAULT 1;
    WHILE i <= 6000000 DO
        UPDATE flights
        SET CANCELLATION_REASON_DESC = CASE CANCELLATION_REASON
            WHEN 'A' THEN 'Airline / Carrier'
            WHEN 'B' THEN 'Weather'
            WHEN 'C' THEN 'National Air System'
            WHEN 'D' THEN 'Security'
            ELSE NULL END
        WHERE flight_id BETWEEN i AND i + 499999;
        SET i = i + 500000;
    END WHILE;
END$$
DELIMITER ;
SET SQL_SAFE_UPDATES = 0;
CALL update_is_delayed();
CALL update_cancel_desc();
SET SQL_SAFE_UPDATES = 1;

