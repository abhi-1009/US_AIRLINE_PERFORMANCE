SELECT COUNT(*) AS total_flights, SUM(CANCELLED) AS total_cancelled,
    ROUND(SUM(CANCELLED)/COUNT(*)*100, 2) AS cancellation_rate_pct, SUM(DIVERTED) AS total_diverted,
    SUM(IS_DELAYED) AS total_delayed, ROUND(SUM(IS_DELAYED)/NULLIF(COUNT(*)-SUM(CANCELLED),0)*100, 2)AS delay_rate_pct,
    ROUND(AVG(CASE WHEN ARRIVAL_DELAY > 0 THEN ARRIVAL_DELAY END), 2) AS avg_delay_when_delayed_min,
    ROUND(AVG(ARRIVAL_DELAY), 2) AS avg_arrival_delay_all_min, MAX(ARRIVAL_DELAY) AS max_arrival_delay_min,
    MIN(ARRIVAL_DELAY) AS min_arrival_delay_min
FROM vw_flights_enriched;

SELECT SUM(IS_DELAYED IS NULL) AS null_is_delayed_count FROM vw_flights_enriched;

SELECT COUNT(*) AS total_flown, SUM(CASE WHEN ARRIVAL_DELAY <= 15 THEN 1 ELSE 0 END) AS on_time_flights,
    ROUND(SUM(CASE WHEN ARRIVAL_DELAY <= 15 THEN 1 ELSE 0 END) / COUNT(*) * 100, 2) AS otp_rate_pct
FROM vw_flights_enriched WHERE CANCELLED = 0;

SELECT COALESCE(CANCELLATION_REASON_DESC, 'Not Cancelled') AS reason, COUNT(*) AS flight_count,
    ROUND(COUNT(*) / SUM(COUNT(*)) OVER() * 100, 2) AS pct_of_all_flights FROM vw_flights_enriched
GROUP BY CANCELLATION_REASON_DESC ORDER BY flight_count DESC;

SELECT AIRLINE_CODE, AIRLINE_NAME, COUNT(*) AS total_flights, SUM(CANCELLED) AS total_cancelled,
    ROUND(SUM(CANCELLED)/COUNT(*)*100, 2) AS cancellation_rate_pct, SUM(IS_DELAYED) AS total_delayed,
    ROUND(SUM(IS_DELAYED)/ NULLIF(COUNT(*)-SUM(CANCELLED),0)*100, 2) AS delay_rate_pct,
    ROUND(AVG(CASE WHEN CANCELLED=0 THEN ARRIVAL_DELAY END), 2) AS avg_arrival_delay_min,
    ROUND(SUM(CASE WHEN CANCELLED=0 AND ARRIVAL_DELAY <= 15 THEN 1 ELSE 0 END) /NULLIF(COUNT(*)-SUM(CANCELLED),0)*100, 2)
    AS otp_rate_pct FROM vw_flights_enriched GROUP BY AIRLINE_CODE, AIRLINE_NAME ORDER BY otp_rate_pct DESC;

SELECT MONTH, COUNT(*) AS total_flights, SUM(CANCELLED) AS total_cancelled,
    ROUND(SUM(CANCELLED)/COUNT(*)*100, 2) AS cancellation_rate_pct, SUM(IS_DELAYED) AS total_delayed,
    ROUND(SUM(IS_DELAYED)/ NULLIF(COUNT(*)-SUM(CANCELLED),0)*100, 2) AS delay_rate_pct,
    ROUND(AVG(CASE WHEN CANCELLED=0 THEN ARRIVAL_DELAY END), 2) AS avg_arrival_delay_min
FROM vw_flights_enriched GROUP BY MONTH ORDER BY MONTH;    
   
   
