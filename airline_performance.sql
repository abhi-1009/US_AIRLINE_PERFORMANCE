SET SQL_SAFE_UPDATES = 1;

ALTER TABLE flights ADD COLUMN SCHEDULED_DEP_TIME TIME AFTER SCHEDULED_DEPARTURE,
ADD COLUMN ACTUAL_DEP_TIME    TIME AFTER DEPARTURE_TIME, ADD COLUMN SCHEDULED_ARR_TIME TIME AFTER SCHEDULED_ARRIVAL,
ADD COLUMN ACTUAL_ARR_TIME    TIME AFTER ARRIVAL_TIME;
DELIMITER $$
CREATE PROCEDURE update_time_columns()
BEGIN
    DECLARE i INT DEFAULT 1;
    WHILE i <= 6000000 DO
        UPDATE flights SET
            SCHEDULED_DEP_TIME = MAKETIME(FLOOR(SCHEDULED_DEPARTURE/100), MOD(SCHEDULED_DEPARTURE,100), 0),
            ACTUAL_DEP_TIME    = CASE WHEN DEPARTURE_TIME IS NOT NULL AND FLOOR(DEPARTURE_TIME/100) < 24 
                                 THEN MAKETIME(FLOOR(DEPARTURE_TIME/100), MOD(DEPARTURE_TIME,100), 0) ELSE NULL END,
            SCHEDULED_ARR_TIME = MAKETIME(FLOOR(SCHEDULED_ARRIVAL/100), MOD(SCHEDULED_ARRIVAL,100), 0),
            ACTUAL_ARR_TIME    = CASE WHEN ARRIVAL_TIME IS NOT NULL AND FLOOR(ARRIVAL_TIME/100) < 24 
                                 THEN MAKETIME(FLOOR(ARRIVAL_TIME/100), MOD(ARRIVAL_TIME,100), 0) ELSE NULL END
        WHERE flight_id BETWEEN i AND i + 499999;
        SET i = i + 500000;
    END WHILE;
END$$
DELIMITER ;
SET SQL_SAFE_UPDATES = 0;
CALL update_time_columns();
SET SQL_SAFE_UPDATES = 1;

