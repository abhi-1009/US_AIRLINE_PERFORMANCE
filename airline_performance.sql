CREATE PROCEDURE update_departure_hour()
BEGIN
    DECLARE i INT DEFAULT 1;
    WHILE i <= 6000000 DO
        UPDATE flights 
        SET DEPARTURE_HOUR = HOUR(SCHEDULED_DEP_TIME)
        WHERE flight_id BETWEEN i AND i + 499999;
        SET i = i + 500000;
    END WHILE;
END$$
